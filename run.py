import csv
import random
import time
from datetime import datetime

# Sample products to use in the sales dialogues
products = [
    {"name": "Smartphone", "features": "latest camera technology, long-lasting battery"},
    {"name": "Laptop", "features": "lightweight design, high-performance processor"},
    {"name": "Headphones", "features": "noise-cancellation, immersive sound experience"},
    {"name": "Smartwatch", "features": "fitness tracking, water-resistant design"},
    {"name": "Tablet", "features": "large screen, fast processing power"},
    {"name": "Gaming Console", "features": "high-definition graphics, multiplayer capabilities"},
    {"name": "Digital Camera", "features": "high megapixel count, image stabilization"},
    {"name": "Fitness Tracker", "features": "activity monitoring, heart rate sensor"},
    {"name": "Portable Speaker", "features": "crystal clear sound, long battery life"},
    {"name": "E-book Reader", "features": "e-ink display, lightweight design"},
]

# Additional sales dialogue lines
salesman_lines = [
    "Our {product} is top-notch, offering {features}. Customers love it!",
    "The {product} is a game-changer, especially with its {features}.",
    "When it comes to {product}, nothing beats our combination of {features}.",
    "You'll be amazed by the performance of our {product}, especially its {features}.",
    "Choosing our {product} means enjoying {features} and much more!",
    "With {features}, our {product} outperforms the competition every time!",
    "The {product} is designed to exceed your expectations, with {features} as its highlight.",
    "If you're looking for {features}, our {product} is the perfect fit!",
    "Experience the future with our {product}, equipped with {features}.",
    "Don't settle for less. Upgrade to our {product} for {features} and more!",
    "Enhance your lifestyle with our {product}. Its {features} are unmatched!",
]

user_lines = [
    "I'm intrigued. Could you elaborate more on that?",
    "Interesting. What sets it apart from similar products in the market?",
    "How does the {product} compare to other options available?",
    "Could you provide some insight into the {features} of the {product}?",
    "I'm curious about the {product}. What makes it stand out?",
    "What's the price range for the {product} with these features?",
    "Can you explain how the {product} performs in real-world scenarios?",
    "I'm looking for something with {features}. Does the {product} meet that criteria?",
    "How does the {product} address common issues in its category?",
    "Are there any special offers or bundles available for the {product}?",
    "Could you recommend accessories that complement the {product}?",
    "Is the {product} compatible with other devices I own?",
    "What warranty options are available for the {product}?",
]

# Function to generate a single sales dialogue
def generate_sales_dialogue(product):
    dialogue = []
    for _ in range(random.randint(2, 5)):  # Random number of exchanges
        salesman_line = random.choice(salesman_lines).format(product=product['name'], features=product['features'])
        user_line = random.choice(user_lines).format(product=product['name'], features=product['features'])
        timestamp = datetime.now().strftime("%Y-%m-%d %H:%M:%S")
        dialogue.append([salesman_line, user_line, timestamp])
    return dialogue

# Function to generate the dataset
def generate_dataset(num_dialogues):
    dataset = []
    for _ in range(num_dialogues):
        product = random.choice(products)
        dataset.extend(generate_sales_dialogue(product))
    return dataset

# Function to save the dataset to a CSV file
def save_to_csv(dataset, filename):
    with open(filename, mode='w', newline='', encoding='utf-8') as file:
        writer = csv.writer(file)
        writer.writerow(["Salesman", "User", "TimeStamp"])
        for dialogue in dataset:
            writer.writerow(dialogue)

# Main function to run the script
def main():
    start_time = time.time()
    dataset = generate_dataset(100)  # Generate 100 dialogues
    save_to_csv(dataset, 'sales_conversations.csv')
    end_time = time.time()
    print(f"Data generated in {end_time - start_time} seconds")

if __name__ == "__main__":
    main()